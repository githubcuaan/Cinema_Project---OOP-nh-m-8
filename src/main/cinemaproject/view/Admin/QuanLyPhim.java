    //put movies into table
    public void showMovieTable()
    {
        ArrayList<Movie> mTable;
        try {
            mTable = mList();
        }
        catch(SQLException e)
        {
            System.out.println("vkl loi : "+e.getMessage());
            return;
        }

        DefaultTableModel model = (DefaultTableModel) BangPhim.getModel();
        model.setRowCount(0);

        for(Movie m: mTable)
        {
            model.addRow(new Object[]{
                m.getId(),
                m.getName(),
                m.getGenre(),
                m.getDirector(),
                m.getReleaseDate(),
                m.getDuration()
            });
        }

        //tự động điều chỉnh kích thước các cột
        BangPhim.setAutoResizeMode(JTable.AUTO_RESIZE_ALL_COLUMNS);
    }
    
