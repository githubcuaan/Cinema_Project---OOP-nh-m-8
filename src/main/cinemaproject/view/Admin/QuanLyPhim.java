    private void ThemButActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThemButActionPerformed
        //lấy các thông tin từ textfield
        String name = TenPhimText.getText().trim();
        String genre = TheLoaiText.getText().trim();
        String Director = DaoDienText.getText().trim();
        String releaseDate = NgayCongChieuText.getText().trim();
        String durationText = ThoiLuongText.getText().trim();

        //kiểm tra các điều kiện
        if (name.isEmpty() || genre.isEmpty() || Director.isEmpty() || releaseDate.isEmpty() || ThoiLuongText.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin.");
            return;
        }
        
        //chuyển thời lượng về số nguyên
        int duration;
        try
        {
            duration = Integer.parseInt(durationText);
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Thời lượng phải là một số hợp lệ.");
            return;
        }

        //tạo một đối tượng phim mới
        Movie newMovie = new Movie(name, genre, releaseDate, Director, duration);
        
        //thêm phim mới vào cơ sở dữ liệu
        movieController.addMovie(newMovie);
        
        //Thông báo thêm thành công
        JOptionPane.showMessageDialog(this, "Thêm phim thành công.");
    
        //hiển thị lại bảng phim
        showMovieTable();
    }//GEN-LAST:event_ThemButActionPerformed


