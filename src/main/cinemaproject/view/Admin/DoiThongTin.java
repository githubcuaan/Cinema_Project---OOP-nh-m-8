    private void ThayDoiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThayDoiActionPerformed
        String tenKhach = name.getText().trim();
        String soDienThoai = phone.getText().trim();
        String diahiEmail = email.getText().trim();

        // Kiểm tra dữ liệu đầu vào
        if (tenKhach.isEmpty() || soDienThoai.isEmpty() || diahiEmail.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Thay đổi thông tin
        user.setName(tenKhach);
        user.setPhone(soDienThoai);
        user.setEmail(diahiEmail);

        // Cập nhật
        boolean success = customerController.updateCustomer(user);

        // Thông báo nếu thành công
        if (success) {
            JOptionPane.showMessageDialog(this, "Thông tin khách hàng đã được cập nhật thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin khách hàng thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ThayDoiActionPerformed

    private void ThayDoi1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ThayDoi1ActionPerformed
        String pass = matkhau.getText().trim();
        String pass2 = matkhau2.getText().trim();

        if(pass.isEmpty() || pass2.isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Vui lòng điền đầy đủ thông tin!", "Lỗi", JOptionPane.ERROR_MESSAGE);
            return;
        }

        if(!pass.equals(pass2))
        {
            JOptionPane.showMessageDialog(this, "Mật khẩu nhập lại không giống nhau!", "NO", JOptionPane.INFORMATION_MESSAGE);
        }

        //thay doi
        user.setPassword(pass);

        // cap nhat
        boolean success = customerController.updateCustomer(user);

        if (success) {
            JOptionPane.showMessageDialog(this, "Thông tin khách hàng đã được cập nhật thành công!");
        } else {
            JOptionPane.showMessageDialog(this, "Cập nhật thông tin khách hàng thất bại!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ThayDoi1ActionPerformed

